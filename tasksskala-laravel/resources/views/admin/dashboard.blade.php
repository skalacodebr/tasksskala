<script src="https://cdn.jsdelivr.net/npm/chart.js"></script>
<script>
document.addEventListener('DOMContentLoaded', function() {
    // Projects Status Pie Chart
    const projectsStatusData = @json($projetosPorStatus);
    const statusLabels = Object.keys(projectsStatusData);
    const statusValues = Object.values(projectsStatusData);
    
    // Traduzir labels para português
    const statusTranslations = {
        'planejamento': 'Planejamento',
        'em_andamento': 'Em Andamento',
        'em_teste': 'Em Teste',
        'aprovacao_app': 'Aprovação App',
        'concluido': 'Concluído',
        'cancelado': 'Cancelado'
    };
    
    const translatedLabels = statusLabels.map(label => statusTranslations[label] || label);
    
    // Cores para cada status
    const statusColors = {
        'planejamento': '#9CA3AF',
        'em_andamento': '#3B82F6',
        'em_teste': '#F59E0B',
        'aprovacao_app': '#8B5CF6',
        'concluido': '#10B981',
        'cancelado': '#EF4444'
    };
    
    const backgroundColors = statusLabels.map(label => statusColors[label] || '#6B7280');
    
    const projectsCtx = document.getElementById('projectsStatusChart').getContext('2d');
    new Chart(projectsCtx, {
        type: 'pie',
        data: {
            labels: translatedLabels,
            datasets: [{
                data: statusValues,
                backgroundColor: backgroundColors,
                borderWidth: 2,
                borderColor: '#ffffff'
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            plugins: {
                legend: {
                    position: 'bottom',
                    labels: {
                        padding: 20,
                        usePointStyle: true
                    }
                },
                tooltip: {
                    callbacks: {
                        label: function(context) {
                            const label = context.label || '';
                            const value = context.parsed;
                            const total = context.dataset.data.reduce((a, b) => a + b, 0);
                            const percentage = ((value / total) * 100).toFixed(1);
                            return `${label}: ${value} (${percentage}%)`;
                        }
                    }
                }
            }
        }
    });

    // Collaborators Tasks Bar Chart
    const collaboratorsData = @json($colaboradoresTarefas);
    const collaboratorNames = collaboratorsData.map(item => item.nome);
    const tasksCounts = collaboratorsData.map(item => item.tarefas_count);
    
    const collaboratorsCtx = document.getElementById('collaboratorsTasksChart').getContext('2d');
    new Chart(collaboratorsCtx, {
        type: 'bar',
        data: {
            labels: collaboratorNames,
            datasets: [{
                label: 'Número de Tarefas',
                data: tasksCounts,
                backgroundColor: '#3B82F6',
                borderColor: '#1E40AF',
                borderWidth: 1,
                borderRadius: 4,
                borderSkipped: false,
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            scales: {
                y: {
                    beginAtZero: true,
                    ticks: {
                        stepSize: 1
                    }
                },
                x: {
                    ticks: {
                        maxRotation: 45,
                        minRotation: 0
                    }
                }
            },
            plugins: {
                legend: {
                    display: false
                },
                tooltip: {
                    callbacks: {
                        label: function(context) {
                            return `${context.parsed.y} tarefa${context.parsed.y !== 1 ? 's' : ''}`;
                        }
                    }
                }
            }
        }
    });
});
</script>
